    public static ArrayList<PositionPoint> getRankingScale(int numberOfStars, int numberOfTeams) {
        numberOfTeams = findScaleForNumberOfTeams(numberOfStars, numberOfTeams);
        return createListOfPositionsAndPoints(numberOfStars, numberOfTeams);
    }

    private static int findScaleForNumberOfTeams(int numberOfTStars, int numberOfTeams) {
        for (; numberOfTeams < 65; numberOfTeams++) {
            if (R[numberOfTStars][numberOfTeams][1] > 0) {
                break;
            }
        }
        return numberOfTeams;
    }

    private static ArrayList<PositionPoint> createListOfPositionsAndPoints(int numberOfStars, int numberOfTeams) {
        ArrayList<PositionPoint> positionAndPoints = new ArrayList<>();
        for (int pos = 1; pos < POSITIONS; pos++) {
            int points = R[numberOfStars][numberOfTeams][pos];
            if (points > 0) {
                positionAndPoints.add(new PositionPoint(pos, points));
            }
        }
        return positionAndPoints;
    }


    public static class PositionPoint {

        private int position;

        private int points;

        PositionPoint(int position, int points) {
            this.position = position;
            this.points = points;
        }

        public int getPosition() {
            return position;
        }

        public int getPoints() {
            return points;
        }

        @Override
        public String toString() {
            return "(" + position + "," + points + ")";
        }
    }
